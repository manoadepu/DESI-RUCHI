package com.desiruchi.service_one.model;

public class Nutrition {
    private int TotalCarbs;
    private int DietaryFiber;
    private int Sugars;
    private int Protien;
    private int totalFat;
    private int cholestrol;
    private int vitaminA;
    private int vitaminC;
    private int calcium;
    private int iron;

    public int getTotalCarbs() {
        return TotalCarbs;
    }

    public void setTotalCarbs(int totalCarbs) {
        TotalCarbs = totalCarbs;
    }

    public int getDietaryFiber() {
        return DietaryFiber;
    }

    public void setDietaryFiber(int dietaryFiber) {
        DietaryFiber = dietaryFiber;
    }

    public int getSugars() {
        return Sugars;
    }

    public void setSugars(int sugars) {
        Sugars = sugars;
    }

    public int getProtien() {
        return Protien;
    }

    public void setProtien(int protien) {
        Protien = protien;
    }

    public int getTotalFat() {
        return totalFat;
    }

    public void setTotalFat(int totalFat) {
        this.totalFat = totalFat;
    }

    public int getCholestrol() {
        return cholestrol;
    }

    public void setCholestrol(int cholestrol) {
        this.cholestrol = cholestrol;
    }

    public int getVitaminA() {
        return vitaminA;
    }

    public void setVitaminA(int vitaminA) {
        this.vitaminA = vitaminA;
    }

    public int getVitaminC() {
        return vitaminC;
    }

    public void setVitaminC(int vitaminC) {
        this.vitaminC = vitaminC;
    }

    public int getCalcium() {
        return calcium;
    }

    public void setCalcium(int calcium) {
        this.calcium = calcium;
    }

    public int getIron() {
        return iron;
    }

    public void setIron(int iron) {
        this.iron = iron;
    }
}
